CREATE PROC SP_GETPLAYERINFO
@AD AS VARCHAR(50),
@TAKIM AS VARCHAR(50),
@LIG AS VARCHAR(50)
AS
BEGIN
    SELECT * FROM OYUNCU_BILGI
    WHERE AD LIKE '%' + @AD + '%'
    AND OYNADIGI_TAKIM LIKE '%' + @TAKIM + '%'
    AND OYNADIGI_LIG LIKE '%' + @LIG + '%'
END

EXEC SP_GETPLAYERINFO
@AD='%', @TAKIM='%', @LIG='Ý'


--*OYUNCU BÝLGÝ TABLOSU ÝLE PERFORMANS TABLOSUNUN BÝRLEÞÝMÝNDEN OLUÞAN SP
CREATE PROC SP_GETPLAYERPERF
@AD AS VARCHAR(50),
@TAKIM AS VARCHAR(50),
@LIG AS VARCHAR(50),
@GOL AS VARCHAR(10)
AS
BEGIN
    SELECT O.AD, O.OYNADIGI_LIG, O.OYNADIGI_TAKIM, O.OYNADIGI_LIG, P.GOL_SAYISI
    FROM OYUNCU_BILGI O
    JOIN PERFORMANS P ON P.AD = O.AD
    WHERE P.GOL_SAYISI LIKE '%'+@GOL+'%'  
	AND O.AD LIKE '%'+@AD+'%' 
	AND O.OYNADIGI_TAKIM LIKE '%'+@TAKIM+'%' 
	AND O.OYNADIGI_LIG LIKE '%'+@LIG+'%'
END

EXEC SP_GETPLAYERPERF
@AD='%', @TAKIM='Arsenal',@LIG='%',@GOL='%'



