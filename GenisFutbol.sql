CASE
	WHEN LEN(CONVERT(varchar, [2020V])) = 6 THEN DEGER + ' bin EURO deðerindeydi'
    ELSE DEGER + ' milyon EURO deðerindeydi'
END AS [Metin]
FROM 
(SELECT AD,
CONVERT(varchar, [2020]) AS [2020V],
CASE
	WHEN LEN(CONVERT(varchar, [2020])) = 6 THEN SUBSTRING(CONVERT(varchar, [2020]), 1, 3)
    WHEN LEN(CONVERT(varchar, [2020])) = 7 THEN LEFT(CONVERT(varchar, [2020]), 1)
    WHEN LEN(CONVERT(varchar, [2020])) = 8 THEN LEFT(CONVERT(varchar, [2020]), 2)
    WHEN LEN(CONVERT(varchar, [2020])) = 9 THEN LEFT(CONVERT(varchar, [2020]), 3)
END AS DEGER
FROM PIYASA_DEGERI
